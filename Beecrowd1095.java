
public class Beecrowd1095 {
	int i;
	int j;
	public static void main(String[] args) {
		Beecrowd1095 s = new Beecrowd1095();
		s.sequencia();
	}
	
	
	public void sequencia() {
		
		i = -2;
		j = 65;
		for(int k = 0; k < j; k += 0) {
			i+=3;
			j -= 5;
			System.out.print("I=" + i +" ");
			System.out.println("J=" + j);
			
			
		}
	}
}
